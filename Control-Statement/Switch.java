
public class Switch {

    public static void main(String[] args) {
        String num = "No Name";
        switch (num) {
            case "Safwan":
                System.out.println("Name is Safwan");
                break;
            case "Mirza":
                System.out.println("Surname is Mirza");
                break;
            default:
                System.out.println(num);
        }
    }

}
