
public class IfElse {
    public static void main(String[] args) {
        String city = "Agra";
        if (city == "Meerut") {
            System.out.println("city is Meerut");
        } else if (city == "Noida") {
            System.out.println("City is Noida");
        } else if (city == "Agra") {
            System.out.println("city is agra");
        } else {
            System.out.println(city);
        }
    }
}
